//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//名称:STM8L052C6T6看门狗配置
//-----------------------------------------------------------------------------
void bsp_watchdog_cfg(void)
{
    
    #ifdef NDEBUG
    M0P_SYSCTRL->PERI_CLKEN_f.WDT = 1;
    M0P_WDT->CON_f.WOV = 10u;
    M0P_WDT->CON_f.WINT_EN = 0;
    M0P_WDT->RST = 0x1E;
    M0P_WDT->RST = 0xE1;
    #endif
}
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//-----------------------------------------------------------------------------
void bsp_watchdog_reload(void)
{
    #ifdef NDEBUG
    M0P_WDT->RST = 0x1E;
    M0P_WDT->RST = 0xE1;
    #endif
}
