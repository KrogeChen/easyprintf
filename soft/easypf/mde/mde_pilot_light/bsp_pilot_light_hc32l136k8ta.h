//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//PB02
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//名称: 指示灯端口配置
//功能: 
//入口: 
//
//出口: 
//-------------------------------------------------------------------------------------------------
void bsp_pilot_light_cfg(void)
{
    M0P_SYSCTRL->PERI_CLKEN_f.GPIO = 1;
//------------------------------------------------------------------------------

    M0P_GPIO->PB02_SEL = 0;
    M0P_GPIO->PB02_SEL = 0;
    M0P_GPIO->PBDIR_f.PB02 = 0;  //output
    M0P_GPIO->PBOUT_f.PB02 = 0;
    
    //PC01，EPF指示
    M0P_GPIO->PC01_SEL = 0;
    M0P_GPIO->PCDIR_f.PC01 = 0;  //output
    M0P_GPIO->PCOUT_f.PC01 = 0;
    //PA05，RTLD指示
    M0P_GPIO->PA05_SEL = 0;
    M0P_GPIO->PADIR_f.PA05 = 0;  //output
    M0P_GPIO->PAOUT_f.PA05 = 0;
}
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//名称: 点亮指示灯
//功能: 
//入口: 灯编号
//
//出口: 无
//-------------------------------------------------------------------------------------------------
void bsp_pilot_light_lighten(sdt_int8u in_lamp_id)
{
    switch(in_lamp_id)
    {
        case 0:
        {
            M0P_GPIO->PBOUT_f.PB02 = 0;
            break;
        }
        case 1:
        {
            M0P_GPIO->PCOUT_f.PC01 = 0;
            break;
        }
        case 2:
        {
            M0P_GPIO->PAOUT_f.PA05 = 0;
            break;
        }
        default:
        {
            break;
        }
    }
}
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//名称: 熄灭指示灯
//功能: 
//入口: 灯编号
//
//出口: 无
//-------------------------------------------------------------------------------------------------
void bsp_pilot_light_dark(sdt_int8u in_lamp_id)
{
    switch(in_lamp_id)
    {
        case 0:
        {
            M0P_GPIO->PBOUT_f.PB02 = 1;
            break;
        }
        case 1:
        {
            M0P_GPIO->PCOUT_f.PC01 = 1;
            break;
        }
        case 2:
        {
            M0P_GPIO->PAOUT_f.PA05 = 1;
            break;
        }
        default:
        {
            break;
        }
    }
}
