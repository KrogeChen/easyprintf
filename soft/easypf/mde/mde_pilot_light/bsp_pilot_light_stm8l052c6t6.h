//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
#define  LEDONEPDIR      PE_DDR
#define  LEDONEGPIO      PE_ODR
#define  LEDONEBIT       0x80
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//名称: 指示灯端口配置
//功能: 
//入口: 
//
//出口: 
//------------------------------------------------------------------------------
void bsp_pilot_light_cfg(void)
{
    LEDONEPDIR |= LEDONEBIT;       //1为输出管脚
    PE_CR1 |= 0x80;
    PE_CR2 &= (~0x80);
}
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//名称: 点亮指示灯
//功能: 
//入口: 灯编号
//
//出口: 无
//------------------------------------------------------------------------------
void bsp_pilot_light_lighten(sdt_int8u in_lamp_id)
{
    switch(in_lamp_id)
    {
        case 0:
        {
            LEDONEGPIO &= ~LEDONEBIT;
            break;
        }
        default:
        {
            break;
        }
    }
}
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//名称: 熄灭指示灯
//功能: 
//入口: 灯编号
//
//出口: 无
//------------------------------------------------------------------------------
void bsp_pilot_light_dark(sdt_int8u in_lamp_id)
{
    switch(in_lamp_id)
    {
        case 0:
        {
            LEDONEGPIO |= LEDONEBIT;
            break;
        }
        default:
        {
            break;
        }
    }
}
